class DataTypeDemo {
    public static void main(String[] args) {
        int count = 10;
        float price = 12.5f;
        double total = 12345.67;
        char grade = 'A';
        boolean active = true;
        System.out.println(count);
        System.out.println(price);
        System.out.println(total);
        System.out.println(grade);
        System.out.println(active);
    }
}

